public class Location {

    private String name;
    private String city;
    private String address;
    private String zipCode;

    public Location() {
    }

    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }

    public String getZipCode() {
        return zipCode;
    }

    public String getAddress() {
        return address;
    }
}
